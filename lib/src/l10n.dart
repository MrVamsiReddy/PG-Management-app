import 'package:flutter/foundation.dart' show SynchronousFuture;
import 'package:flutter/material.dart';

/// Supported app languages. Add a new entry plus its column in [_strings] to
/// extend — nothing else needs to change.
enum AppLanguage {
  english('en', 'English'),
  hindi('hi', 'हिन्दी'),
  telugu('te', 'తెలుగు');

  const AppLanguage(this.code, this.nativeName);
  final String code;
  final String nativeName;

  Locale get locale => Locale(code);

  static AppLanguage fromCode(String? code) => AppLanguage.values
      .firstWhere((e) => e.code == code, orElse: () => AppLanguage.english);
}

/// Tiny map-backed localization. English is the source of truth and the
/// fallback for any missing key or language.
class AppLocalizations {
  const AppLocalizations(this.locale);
  final Locale locale;

  static AppLocalizations of(BuildContext context) =>
      Localizations.of<AppLocalizations>(context, AppLocalizations) ??
      const AppLocalizations(Locale('en'));

  static const delegate = _AppLocalizationsDelegate();
  static const supportedLocales = [Locale('en'), Locale('hi'), Locale('te')];

  String t(String key) {
    final lang = _strings[locale.languageCode] ?? _strings['en']!;
    return lang[key] ?? _strings['en']![key] ?? key;
  }

  /// Maps a stable backend/auth `code:*` string to a localized message. Any
  /// value that isn't a recognised code is returned unchanged (already a
  /// human message).
  /// Localized display text for an enum's English [label] (KYC, visitor,
  /// maintenance, payment, priority, customer statuses). Colors and logic
  /// keep matching on the English label; only the display changes.
  String status(String label) => switch (label) {
        'Due' => t('status.due'),
        'Overdue' => t('status.overdue'),
        'Pending' || 'Pending confirmation' => t('status.pending'),
        'Paid' => t('status.paid'),
        'Rejected' => t('status.rejected'),
        'Partial' => t('status.partial'),
        'Open' => t('status.open'),
        'In progress' => t('status.inProgress'),
        'Resolved' => t('status.resolved'),
        'Low' => t('status.low'),
        'Medium' => t('status.medium'),
        'High' => t('status.high'),
        'Awaiting approval' => t('status.awaiting'),
        'Inside' => t('status.inside'),
        'Checked out' => t('status.checkedOut'),
        'Declined' => t('status.declined'),
        'Verified' => t('status.verified'),
        'Enabled' => t('status.enabled'),
        'Disabled' => t('status.disabled'),
        'Expired' => t('status.expired'),
        'Managing' => t('status.managing'),
        'All' => t('status.all'),
        _ => label,
      };

  String error(String? code) => switch (code) {
        'code:network' => t('err.network'),
        'code:bad_credentials' => t('err.badCredentials'),
        'code:temp_wrong' => t('setpw.tempWrong'),
        'code:weak_password' => t('auth.passwordShort'),
        'code:email_in_use' => t('err.emailInUse'),
        'code:generic' || null => t('err.generic'),
        _ => code,
      };
}

class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();
  @override
  bool isSupported(Locale locale) => _strings.containsKey(locale.languageCode);
  @override
  // Synchronous so the first frame already has strings (no blank flash, and
  // widget tests don't need an extra pump).
  Future<AppLocalizations> load(Locale locale) =>
      SynchronousFuture(AppLocalizations(locale));
  @override
  bool shouldReload(_AppLocalizationsDelegate old) => false;
}

